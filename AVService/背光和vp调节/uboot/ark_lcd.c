int printf_mem(cmd_tbl_t *cmdtp, int flag, int argc, char * const argv[])
{
	int ret, i;
	long unsigned int addr, len;
	unsigned char *p = NULL;

	if(argc < 3)
		return cmd_usage(cmdtp);

	ret = strict_strtoul(argv[1], 16, &addr);
	if(ret < 0){
		printf("error format addr\n");
	}

	if (addr % 4) {
		printf("invalid  addr 0x%lx (should be multiples of 4)\n",addr);
		return 1;
	}

	ret = strict_strtoul(argv[2], 16, &len);
	if(ret < 0){
		printf("read len wrong\n");
	}
	
	p = (unsigned char *)addr;

	for(i = 0; i < len; i++)
	{
		printf("0x%-4x", p[i]);

		if(((i + 1)& 0xf) ==  0)
			printf("\n");
	}
	
	return 0;
}	

