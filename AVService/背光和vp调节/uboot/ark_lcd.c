int reg_read(cmd_tbl_t *cmdtp, int flag, int argc, char * const argv[])
{
	int ret;
	long unsigned int  offset, op_code;
	unsigned int val32;
	if(argc < 2)
		return cmd_usage(cmdtp);

	ret = strict_strtoul(argv[1], 16, &op_code);
	if(ret < 0){
		printf("error format op_code\n");
	}

	ret = strict_strtoul(argv[2], 16, &offset);
	if(ret < 0){
		printf("error format register\n");
	}

	if (offset % 4) {
		printf("invalid reg offset 0x%02lx (should be multiples of 4)\n",offset);
		return 1;
	}
    if (offset > 0x800) { //0x800 is the length of lcd control limit
		printf("invalid reg offset 0x%02lx (exceed range 0x800)\n", offset);
		return 1;
    }
	switch(op_code){
		case 0:
			if(offset > 0x800){
				printf("lcd offset 0x%02lx  out of range\n", offset);
				return 1;				
			}
			printf("lcd reg: 0x%lx, val: 0x%x\n", offset, *((volatile unsigned int *)(LCD_BASE + offset)));
			break;
		case 1:
			if(offset > 0x1f0){
				printf("system offset 0x%02lx  out of range\n", offset);
				return 1;				
			}
			printf("system reg: 0x%lx, val: 0x%x\n", offset, *((volatile unsigned int *)(SYS_BASE + offset)));
			break;
		case 2:
			if(offset > 0x95c){
				printf("itu656 offset 0x%02lx  out of range\n", offset);
				return 1;				
			}
			printf("itu656 reg: 0x%lx, val: 0x%x\n", offset, *((volatile unsigned int *)(ITU656_BASE + offset)));
			break;
		case 3:
			if(offset > 0xffc){
				printf("low interrupt 0x%02lx  out of range\n", offset);
				return 1;				
			}
			printf("low interrupt : 0x%lx, val: 0x%x\n", offset, *((volatile unsigned int *)(VICL_BASE + offset)));
			break;
		case 4:
			if(offset > 0xffc){
				printf("high interrupt 0x%02lx  out of range\n", offset);
				return 1;				
			}
			printf("high interrupt : 0x%lx, val: 0x%x\n", offset, *((volatile unsigned int *)(VICH_BASE + offset)));
			break;
		case 5:
			if(offset > 0xf00){
				printf("jpeg 0x%02lx  out of range\n", offset);
				return 1;				
			}
			printf("jpeg : 0x%lx, val: 0x%x\n", offset, *((volatile unsigned int *)(JPEG_BASE + offset)));
			break;
		default:
			printf("no this operation code\n");
			break;			
	}

	return 0;
}

