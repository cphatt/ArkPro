static void ark_back_car_in(void);
static void ark_back_car_quit(void);

static void ark_back_car_in(void)
{
	printf("ark_back_car_in\n");
	GPIO_IRQ_Request(5,euHighLevelTrig, ark_back_car_quit);
	if (backcar_flag) {
		//backlight off
		SetGPIOPadData(81, euDataLow);
		Itu656DirectToLcd();
		udelay(100000);
		SetGPIOPadData(81, euDataHigh);
	}
}

static void ark_back_car_quit(void)
{
	printf("ark_back_car_quit\n");
	GPIO_IRQ_Request(5,euLowLevelTrig, ark_back_car_in);
	if (!backcar_flag) {
		SetGPIOPadData(81, euDataLow);
		Itu656DirectToLcdDeinit();
		udelay(100000);
		SetGPIOPadData(81, euDataHigh);		
	}
}

void ark_detect_back_car(void)
{
	SetGPIODataDirection(5,euInputPad);
	SetGPIODataDirection(81,euOutputPad);
	if(euDataLow == GetGPIOPadData(5))/*low:car reverse on / high:car reverse off*/
	{	
		printf("##back car ...\n");
		ark_back_car_in();
		backcar_flag = 0;
	}else{	
		printf("###not back car ...\n");
		ark_back_car_quit();
	}
	request_irq(GPIO_INT, PRIORITY_FIFTEEN, GPIOIntHander); 
}

