void ark_disp_set_lcd_switch_to_itu656_timing(void)
{
    // CLCD_VIDEO2_CONTROL REG0x320
    rLCD_VIDEO2_CTL |= (1<<11); // use itu656 clock for pre-fifo clock
}

void ark_disp_set_lcd_switch_to_normal_timing(void)
{
    // CLCD_VIDEO2_CONTROL REG0x320
    rLCD_VIDEO2_CTL &= ~(1<<11);  // use axi clk for pre-fifo clock
}

int backcar_flag = 1; //0 backcar; 1 not backcar
void Itu656DirectToLcd(void)
{
    int reg;

	ark_disp_select_itu656_pad(); 

    // set itu656 bypass mode regs
    ark_disp_config_tvenc_itu656_direct_in();
    // enable to generate itu656 no signal detect intr when no signal at
    // itu656 input

    // set to use itu656in clk instead of axi clk for pre-fifo clk
    ark_disp_set_lcd_switch_to_itu656_timing();

	// reference codes
	// open video2 layer on LCD 
	rLCD_VIDEO2_WIN_SIZE = (288<<12) | (720<<0);
	rLCD_VIDEO2_SIZE = (g_screen_info->height<<12) | (g_screen_info->width<<0);
	rLCD_VIDEO2_SCALE_CTL = 1<<7; // scaler UV select, 1=YCbCr

	//open video2
	//close osd1
	if (IS_TVENC_SCREEN(g_screen_info)) {
		rLCD_TV_CONTROL |= (1 << 3);
		rLCD_TV_CONTROL &= ~(1 << 4);	
	} else {
		rLCD_CONTROL |= (1 << 6);
		rLCD_CONTROL &= ~(1 << 7);	
	}	

    ark_itu656_set_direct_data_to_lcd_enable(1);
	
	backcar_flag = 0;
}

void Itu656DirectToLcdDeinit(void)
{
    int reg;

	//close video2
	//open osd1
	if (IS_TVENC_SCREEN(g_screen_info)) {
		rLCD_TV_CONTROL &= ~(1 << 3);
		rLCD_TV_CONTROL |= (1 << 4);	
	} else {
		rLCD_CONTROL &= ~(1 << 6);
		rLCD_CONTROL |= (1 << 7);	
	}	
	
	ark_itu656_set_direct_data_to_lcd_enable(0);
	ark_disp_set_lcd_switch_to_normal_timing();
	
	backcar_flag = 1;
}

